#include "pch.h"
#include "CClrTestClass.h"

using namespace ClrTest;

double CClrTestClass::Sum(double p1, double p2)
{
    return p1 + p2;
}

double CClrTestClass::Sub(double p1, double p2)
{
    return p1 - p2;
}

double CClrTestClass::Mul(double p1, double p2)
{
    return p1 * p2;
}

double CClrTestClass::Div(double p1, double p2)
{
    return p1 / p2;
}
